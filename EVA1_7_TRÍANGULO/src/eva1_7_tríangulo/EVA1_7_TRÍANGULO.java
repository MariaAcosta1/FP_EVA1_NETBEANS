
package eva1_7_tríangulo;

public class EVA1_7_TRÍANGULO {

    public static void main(String[] args) {
        //AREA TRIANGULO
        //AREA = (BASE X ALTURA) ENTRE 2;
        double area, base, altura;//tres variables de tipo doble
        //INICIALIZACIÓN;
        base = 10;
        altura = 8;
        area = (base * altura) / 2.0;//CUIDADO CON DIVIDIR ENTRE ENTEROS
        System.out.println("El area de un traingulo base 10 y altura 8 es: ");
        System.out.println(area);
    }
    
}
